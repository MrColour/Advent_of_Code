void	clear_reg(int *registers, int size)
{
	int	i;

	i = 0;
	while (i < size)
		registers[i++] = 0;
}

int		get_value(int *memory, int mode, int value)
{
	int	result;

	if (mode == 0)
		result = memory[value];
	if (mode == 1)
		result = value;
	return (result);
}

